// Utility functions for blog management
export const getBlogPostBySlug = (slug) => {
  return expandedBlogPosts.find(post => post.slug === slug);
};

export const getBlogPostsByCategory = (category) => {
  if (category === 'all') return expandedBlogPosts;
  return expandedBlogPosts.filter(post => post.category === category);
};

export const getFeaturedBlogPosts = () => {
  return expandedBlogPosts.filter(post => post.featured);
};

export const getRecentBlogPosts = (limit = 5) => {
  return expandedBlogPosts
    .sort((a, b) => new Date(b.date) - new Date(a.date))
    .slice(0, limit);
};

export const searchBlogPosts = (searchTerm) => {
  const term = searchTerm.toLowerCase();
  return expandedBlogPosts.filter(post => 
    post.title.toLowerCase().includes(term) ||
    post.excerpt.toLowerCase().includes(term) ||
    post.tags.some(tag => tag.toLowerCase().includes(term))
  );
};
